package _08_DataCollection;

public class _01_arrays {

	public static void main(String[] args) {
		
		String[] names = {"Paul", "Sydney", "John", "Amanda"};
		
		System.out.println(names[3]);
		System.out.println(names[0]);
	
	
		//favorite foods
		String[] foods = {"pizza", "burgers", "chili", "tacos"};
		
		System.out.println(foods[2]);
		System.out.println(foods[0]);
		
		//numbers: ages of family
		//example 1
		String[] familyAges = {"3", "1"};
		
		System.out.println(familyAges[0]);
		System.out.println(familyAges[1]);
		
		//example 2
		int[] ages = {3, 1};
		System.out.println(ages[1]);
		
		
		
		
	}

}
